export function getScenariosByType(type, count=5) {
  const pool = queryQuestScenarios.filter(s => s.type === type);
  return [...pool].sort(() => Math.random()-0.5).slice(0, count);
}

export function getRandomScenarios(count=5) {
  const sqlCount = Math.ceil(count * 0.6);
  const regexCount = count - sqlCount;
  const sql = getScenariosByType('sql', sqlCount);
  const regex = getScenariosByType('regex', regexCount);
  return [...sql, ...regex].sort(() => Math.random()-0.5);
}
